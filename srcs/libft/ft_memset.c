#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*copy;

	i = 0;
	copy = (char *)s;
	while (n > 0)
	{
		copy[i] = c;
		i++;
		n--;
	}
	return (s);
}
